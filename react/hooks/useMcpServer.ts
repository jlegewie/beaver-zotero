/**
 * Standalone Node.js script that bridges MCP stdio transport to Beaver's HTTP
 * endpoint. Written to the Zotero data directory when MCP is enabled.
 * MCP clients like Claude Desktop launch this as a subprocess.
 */
const MCP_BRIDGE_SCRIPT = String.raw`#!/usr/bin/env node
// Beaver MCP stdio bridge
// Connects MCP clients (e.g. Claude Desktop) to Beaver's HTTP endpoint in Zotero.
// Auto-generated by Beaver. Do not edit; it may be overwritten on updates.
//
// Usage: node beaver-mcp-stdio.mjs [port]
//
import { createInterface } from "node:readline";
import { request } from "node:http";

const PORT = process.argv[2] || process.env.BEAVER_MCP_PORT || "23119";

function post(body) {
  return new Promise((resolve, reject) => {
    const req = request({
      hostname: "127.0.0.1",
      port: PORT,
      path: "/beaver/mcp",
      method: "POST",
      headers: { "Content-Type": "application/json", "Accept": "application/json", "Content-Length": Buffer.byteLength(body) },
    }, (res) => {
      let data = "";
      res.on("data", (chunk) => (data += chunk));
      res.on("end", () => resolve(data));
    });
    req.on("error", reject);
    req.write(body);
    req.end();
  });
}

const rl = createInterface({ input: process.stdin, terminal: false });

rl.on("line", async (line) => {
  if (!line.trim()) return;
  let msg;
  try {
    msg = JSON.parse(line);
  } catch {
    return;
  }

  const isNotification = msg.id === undefined || msg.id === null;

  try {
    const response = await post(line);
    if (!isNotification && response) {
      process.stdout.write(response + "\n");
    }
  } catch (err) {
    if (!isNotification) {
      process.stdout.write(JSON.stringify({
        jsonrpc: "2.0",
        id: msg.id,
        error: {
          code: -32000,
          message: "Cannot reach Beaver in Zotero. Is Zotero running? (" + err.message + ")",
        },
      }) + "\n");
    }
  }
});

rl.on("close", () => process.exit(0));
`;

/**
 * Returns the path where the bridge script is (or will be) written.
 */
export function getMcpBridgeScriptPath(): string {
    return PathUtils.join(Zotero.DataDirectory.dir, 'beaver-mcp-stdio.mjs');
}

/**
 * Write the stdio bridge script to disk and return its absolute path.
 */
export async function ensureMcpBridgeScript(): Promise<string> {
    const scriptPath = getMcpBridgeScriptPath();
    await IOUtils.writeUTF8(scriptPath, MCP_BRIDGE_SCRIPT);
    return scriptPath;
}

