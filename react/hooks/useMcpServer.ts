/**
 * Handler for the MCP `read_attachment` tool.
 *
 * Exported so the dev-only `/beaver/test/read-attachment` endpoint can
 * exercise the exact tool code path (page-argument validation, document
 * request, page-window slicing) in automated tests.
 */
export async function handleReadAttachment(args: any): Promise<any> {
    const MAX_PAGES = 30;

    const parsed = parseItemId(args.attachment_id);
    if (!parsed) {
        return mcpError(
            `Invalid attachment_id format: "${args.attachment_id}". Expected format: <library_id>-<zotero_key> (e.g., "1-ABC12345").`,
        );
    }

    const startPage = args.start_page ?? 1;
    if (!Number.isInteger(startPage) || startPage < 1) {
        return mcpError('start_page must be a positive integer.');
    }

    const requestedEndPage = args.end_page ?? (startPage + MAX_PAGES - 1);
    if (!Number.isInteger(requestedEndPage) || requestedEndPage < 1) {
        return mcpError('end_page must be a positive integer.');
    }
    if (requestedEndPage < startPage) {
        return mcpError('end_page must be greater than or equal to start_page.');
    }

    const endPage = args.end_page != null
        ? Math.min(requestedEndPage, startPage + MAX_PAGES - 1)
        : startPage + MAX_PAGES - 1;

    const wsRequest: WSZoteroDocumentRequest = {
        event: 'zotero_document_request',
        request_id: generateRequestId(),
        attachment: {
            library_id: parsed.libraryId,
            zotero_key: parsed.key,
            library_ref: libraryRefForLibraryID(parsed.libraryId) ?? undefined,
        },
        mode: 'markdown',
    };

    const response: WSZoteroDocumentResponse = await handleZoteroDocumentRequest(wsRequest);

    if (response.error || !response.result) {
        return mcpError(response.error ?? 'Failed to read attachment');
    }
    const result = response.result;
    const markdownDocument = result.content_kind === 'epub' || result.content_kind === 'snapshot'
        ? domDocumentToMarkdownPages(result)
        : result.content_kind === 'text'
            ? null
            : result.mode === 'markdown'
                ? {
                    pageCount: result.document.pageCount,
                    pages: result.document.pages.map((page) => ({
                        pageNumber: page.index + 1,
                        markdown: page.markdown ?? '',
                    })),
                }
                : null;
    if (!markdownDocument) {
        return mcpError('Attachment read returned an unsupported document format.');
    }

    const totalPages = markdownDocument.pageCount;
    if (Number.isInteger(totalPages) && totalPages >= 0 && startPage > totalPages) {
        return mcpError(`Requested start_page ${startPage} is out of range; attachment has ${totalPages} pages.`);
    }

    const requestedPages = markdownDocument.pages.filter(
        (page) => page.pageNumber >= startPage && page.pageNumber <= endPage,
    );
    if (requestedPages.length === 0) {
        return mcpError(`Requested page window ${startPage}-${endPage} is out of range or contains no extractable pages.`);
    }

    // Build plain text response with <pageN> tags
    const actualEnd = requestedPages.length > 0
        ? requestedPages[requestedPages.length - 1].pageNumber
        : startPage;
    const header = `Attachment: ${args.attachment_id} | Total pages: ${markdownDocument.pageCount ?? 'unknown'} | Showing pages ${startPage}-${actualEnd}`;
    const pageTexts = requestedPages.map(
        (p) => `<page${p.pageNumber}>\n${p.markdown}\n</page${p.pageNumber}>`,
    );

    return [header, '', ...pageTexts].join('\n');
}

interface AttachmentMarkdownPage {
    pageNumber: number;
    markdown: string;
}

type DomReadItem = {
    kind: string;
    sectionIndex: number;
    order: number;
    text?: string;
    level?: number;
    sentences?: Array<{ text: string }>;
    pageNumber?: number;
};

// EPUB and snapshot both carry stamped per-item page numbers. Grouping items by
// that coordinate keeps MCP reads aligned with backend read pagination.
function domDocumentToMarkdownPages(
    document: Extract<NonNullable<WSZoteroDocumentResponse['result']>, { content_kind: 'epub' | 'snapshot' }>,
): { pageCount: number; pages: AttachmentMarkdownPage[] } {
    const pagesByNumber = new Map<number, DomReadItem[]>();
    const orderedItems = document.sections
        .slice()
        .sort((a, b) => a.index - b.index)
        .flatMap((section) => section.items
            .slice()
            .sort((a, b) => a.order - b.order)
            .map((item) => ({
                ...item,
                pageNumber: item.pageNumber ?? section.index + 1,
            })));

    for (const item of orderedItems) {
        const pageNumber = item.pageNumber ?? 1;
        const pageItems = pagesByNumber.get(pageNumber);
        if (pageItems) {
            pageItems.push(item);
        } else {
            pagesByNumber.set(pageNumber, [item]);
        }
    }

    // An item is stamped with the page of its first text node, so a single item
    // longer than the synthetic page interval (a long <p>/<pre> or a linearized
    // table) spans interior pages that no item is stamped to.
    const observedPageCount = Math.max(0, ...Array.from(pagesByNumber.keys()));
    const pageCount = Math.max(document.pageCount ?? observedPageCount, observedPageCount);
    const pages: AttachmentMarkdownPage[] = [];
    for (let pageNumber = 1; pageNumber <= pageCount; pageNumber++) {
        const items = pagesByNumber.get(pageNumber) ?? [];
        pages.push({
            pageNumber,
            markdown: items
                .map(domItemToMarkdown)
                .filter((text) => text.length > 0)
                .join('\n\n'),
        });
    }
    return { pageCount, pages };
}

function domItemToMarkdown(item: { kind: string; text?: string; level?: number; sentences?: Array<{ text: string }> }): string {
    const text = normalizeMarkdownText(
        item.text ?? item.sentences?.map((sentence) => sentence.text).join(' ') ?? '',
    );
    if (!text) return '';

    switch (item.kind) {
        case 'section_header': {
            const level = Math.min(Math.max(item.level ?? 2, 1), 6);
            return `${'#'.repeat(level)} ${text}`;
        }
        case 'list_item':
            return `- ${text}`;
        case 'caption':
            return `_${text}_`;
        case 'footnote':
            return `[^] ${text}`;
        default:
            return text;
    }
}

function normalizeMarkdownText(text: string): string {
    return text.replace(/\s+/g, ' ').trim();
}

/**
 * Handler for the MCP `read_note` tool.
 *
 * Exported so dev-only endpoints can exercise the same MCP adapter used by
 * external clients.
 */
export async function handleReadNote(args: any): Promise<any> {
    args = args ?? {};
    if (!args.note_id || typeof args.note_id !== 'string') {
        return mcpError('note_id is required and must be a string.');
    }

    const wsRequest: WSReadNoteRequest = {
        event: 'read_note_request',
        request_id: generateRequestId(),
        note_id: args.note_id,
        offset: args.offset,
        limit: args.limit,
    };

    const response: WSReadNoteResponse = await handleReadNoteRequest(wsRequest);

    if (!response.success) {
        return mcpError(response.error ?? 'Failed to read note');
    }

    return {
        note_id: response.note_id,
        title: response.title ?? null,
        parent_item_id: response.parent_item_id ?? null,
        parent_title: response.parent_title ?? null,
        total_lines: response.total_lines ?? null,
        lines_returned: response.lines_returned ?? null,
        has_more: response.has_more ?? false,
        next_offset: response.next_offset ?? null,
        content: response.content ?? '',
        cited_items: (response.cited_items ?? []).map((item) => ({
            item_id: `${item.library_id}-${item.zotero_key}`,
            item_type: item.item_type,
            title: item.title ?? null,
        })),
    };
}

function buildNoopTimeoutContext(): TimeoutContext {
    return {
        signal: new AbortController().signal,
        timeoutSeconds: 120,
        startTime: Date.now(),
    };
}

/**
 * Handler for the MCP `create_note` tool.
 *
 * The host MCP client owns approval UX, so this adapter validates and then
 * executes directly instead of entering Beaver's deferred-action queue.
 */
export async function handleCreateNote(args: any): Promise<any> {
    args = args ?? {};
    const title = typeof args.title === 'string' ? args.title.trim() : '';
    const content = typeof args.content === 'string' ? args.content : '';

    if (!title) {
        return mcpError('title is required and cannot be empty.');
    }
    if (!content.trim()) {
        return mcpError('content is required and cannot be empty.');
    }

    const effectiveCollection = args.parent_id ? undefined : args.collection;
    const actionData = {
        title,
        content,
        parent_item_id: args.parent_id,
        library: args.library,
        collection: effectiveCollection,
    };

    const validateRequest: WSAgentActionValidateRequest = {
        event: 'agent_action_validate',
        request_id: generateRequestId(),
        action_type: 'create_note',
        action_data: actionData,
    };

    const validation = await validateCreateNoteAction(validateRequest);
    if (!validation.valid) {
        return mcpError(validation.error ?? 'Create note validation failed');
    }

    const mergedActionData = {
        ...actionData,
        ...(validation.normalized_action_data ?? {}),
    };

    const executeRequest: WSAgentActionExecuteRequest = {
        event: 'agent_action_execute',
        request_id: generateRequestId(),
        action_type: 'create_note',
        action_data: mergedActionData,
    };

    const response: WSAgentActionExecuteResponse = await executeCreateNoteAction(
        executeRequest,
        buildNoopTimeoutContext(),
    );

    if (!response.success) {
        return mcpError(response.error ?? 'Failed to create note');
    }

    const result = response.result_data ?? {};
    const libraryId = result.library_id;
    const noteKey = result.zotero_key;
    const citedItemsData = result.cited_items_data ?? {};

    return {
        note_id: libraryId != null && noteKey ? `${libraryId}-${noteKey}` : null,
        parent_item_id: libraryId != null && result.parent_key ? `${libraryId}-${result.parent_key}` : null,
        related_item_id: libraryId != null && result.related_item_key ? `${libraryId}-${result.related_item_key}` : null,
        collection_key: result.collection_key ?? null,
        note_content: result.note_content ?? null,
        warning: result.warning ?? null,
        citation_issues: {
            invalid_keys: citedItemsData.invalid_keys ?? [],
            errors: citedItemsData.errors ?? [],
        },
    };
}

async function handleGetItemDetails(args: any): Promise<any> {
    const itemIds: string[] = args.item_ids;
    if (!itemIds || itemIds.length === 0) {
        return mcpError('item_ids must be a non-empty array.');
    }
    if (itemIds.length > 25) {
        return mcpError(`Too many items requested (${itemIds.length}). Maximum is 25.`);
    }

    const wsRequest: WSGetMetadataRequest = {
        event: 'get_metadata_request',
        request_id: generateRequestId(),
        item_ids: itemIds,
        include_attachments: args.include_attachments ?? false,
        include_notes: args.include_notes ?? false,
    };

    const response: WSGetMetadataResponse = await handleGetMetadataRequest(wsRequest);

    if (response.error) {
        return mcpError(`Failed to get item details: ${response.error}`);
    }

    // Enrich items with zotero_uri, citation keys, and transform attachments
    const items = await Promise.all(response.items.map(async (item) => {
        const parsed = parseItemId(item.item_id);

        // Add zotero://select URI
        if (parsed) {
            const zoteroUri = getZoteroSelectURI(parsed.libraryId, parsed.key);
            if (zoteroUri) item.zotero_uri = zoteroUri;
        }

        // Add citation_key (BetterBibTeX-aware) if not already present
        if (!item.citationKey && parsed) {
            try {
                const zoteroItem = await Zotero.Items.getByLibraryAndKeyAsync(parsed.libraryId, parsed.key);
                if (zoteroItem) {
                    const citationKey = await getCitationKeyFromItem(zoteroItem);
                    if (citationKey) item.citationKey = citationKey;
                }
            } catch { /* best-effort */ }
        }

        if (item.attachments && Array.isArray(item.attachments)) {
            item.attachments = item.attachments.map((a: any) => {
                return {
                    attachment_id: getMcpAttachmentId(a),
                    filename: a.filename || null,
                    content_type: a.content_type ?? a.contentType ?? null,
                    content_kind: a.content_kind ?? null,
                    page_count: a.page_count ?? null,
                    annotations_count: a.annotations_count,
                    status: getMcpAttachmentStatus(a),
                };
            });
        }
        if (item.notes && Array.isArray(item.notes)) {
            item.notes = item.notes.map((n: any) => ({
                item_id: n.item_id,
                title: n.title || null,
                parent_item_id: n.parent_item_id ?? null,
                parent_title: n.parent_title ?? null,
                date_modified: n.date_modified ?? null,
            }));
        }
        // Remove internal fields not useful for MCP consumers
        delete item.item_metadata_hash;
        delete item.zotero_version;
        delete item.zotero_synced;
        delete item.item_json;
        return item;
    }));

    return {
        items,
        not_found: response.not_found,
    };
}

async function handleListCollections(args: any): Promise<any> {
    const limit = Math.min(Math.max(1, args.limit ?? 50), 100);
    const offset = Math.max(0, args.offset ?? 0);

    // Parse library parameter — can be a name or ID string
    let libraryId: number | string | null = null;
    if (args.library != null) {
        const parsed = parseInt(args.library, 10);
        libraryId = isNaN(parsed) ? args.library : parsed;
    }

    const wsRequest: WSListCollectionsRequest = {
        event: 'list_collections_request',
        request_id: generateRequestId(),
        library_id: libraryId,
        parent_collection_key: args.parent_collection ?? null,
        include_item_counts: args.include_item_counts ?? true,
        limit,
        offset,
    };

    const response: WSListCollectionsResponse = await handleListCollectionsRequest(wsRequest);

    if (response.error) {
        return mcpError(`Failed to list collections: ${response.error}`);
    }

    const hasMore = offset + limit < response.total_count;

    return {
        total_count: response.total_count,
        has_more: hasMore,
        next_offset: hasMore ? offset + limit : null,
        // library_id/library_name carry the library scope each collection row needs
        // (used when building the hydrated collection-list view for rendering).
        library_id: response.library_id,
        library_name: response.library_name,
        collections: response.collections.map((c) => ({
            collection_key: c.collection_key,
            name: c.name,
            item_count: c.item_count,
            subcollection_count: c.subcollection_count,
        })),
    };
}

